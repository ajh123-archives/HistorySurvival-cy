    SkyRenderer::SkyRenderer()
    {
        m_shader = ShaderManager::Get().getShader<SkyShader>("sky");

        m_mesh.setAttributes({{GL::Type::VEC3}});
        m_mesh.setVertices(Vertices, 108);
        m_mesh.setDrawCount(36);
    }

    void SkyRenderer::render()
    {
        glDepthFunc(GL_LEQUAL);
        auto camera = LocalGame::Get().getCamera();
        m_shader.use();
        m_shader.setUniform<"projection_view">(camera.getProjMat() * glm::mat4(glm::mat3(camera.getViewMat())));
        m_mesh.bind();
        glDrawArrays(GL_TRIANGLES, 0, 36);
    }
